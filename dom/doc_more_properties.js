//more slectores api properties

document.title // get or set the title of the document

document.URL // get the URL of the document

document.referrer // get the URL of the document that linked to the current document

document.domain // get or set the domain of the document


/* From the document object you can get the body and head Element nodes */

document.documentElement //get the root element of the document

document.body //get the body element of the document

document.head //get the head element of the document


/* list of all the element nodes of a particular type */

document.links //list of all the anchor elements in the document

document.images //list of all the image elements in the document

document.forms //list of all the form elements in the document

document.cookie //get or set the cookies associated with the document

document.lastModified //get the date and time the document was last modified

